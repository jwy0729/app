
class Information():
    @staticmethod
    def setCreateTime(self,time):
        self.CreateTime = time
    @staticmethod
    def getCreateTime(self):
        return self.CreateTime
    @staticmethod
    def setname(self,name):
        self.name=name
    @staticmethod
    def getname(self):
        return self.name
    @staticmethod
    def setno(self,no):
        self.no=no
    @staticmethod
    def getno(self):
        return self.no
    @staticmethod
    def setdes(self,des):
        self.des=des
    @staticmethod
    def getdes(self):
        return self.des
    @staticmethod
    def settype(self,type):
        self.type=type
    @staticmethod
    def gettype(self):
        return self.type
